#[macro_export]
macro_rules! max {
    ($x: expr) => ($x);
    ($x: expr, $( $y: expr ),+) => {
        std::cmp::max($x, max!($( $y ),+))
    }
}
#[macro_export]
macro_rules! min {
    ($x: expr) => ($x);
    ($x: expr, $( $y: expr ),+) => {
        std::cmp::min($x, min!($( $y ),+))
    }
}
#[derive(Default)]
struct Solver {}
impl Solver {
    #[fastout]
    fn solve(&mut self) {
        // let mut stdin = LineSource::new(BufReader::new(io::stdin()));
        // macro_rules! input(($($tt:tt)*) => (proconio::input!(from &mut stdin, $($tt)*)));
        input! {
            N: i32,
            S:String,
        }

        let chars = S.chars().collect::<Vec<char>>();

        let mut ans = 0;

        for i in 0..N {
            let left = &chars[0..i as usize];
            let right = &chars[i as usize..];
            let left_set = left.iter().collect::<BTreeSet<_>>();
            let right_set = right.iter().collect::<BTreeSet<_>>();

            let intersection = left_set.intersection(&right_set).collect::<BTreeSet<_>>();
            let count = intersection.len();

            ans = max!(ans, count);
        }

        println!("{}", ans);
    }
}

fn main() {
    std::thread::Builder::new()
        .stack_size(128 * 1024 * 1024)
        .spawn(|| Solver::default().solve())
        .unwrap()
        .join()
        .unwrap();
}
