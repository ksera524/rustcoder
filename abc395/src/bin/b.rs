#[macro_export]
macro_rules! max {
    ($x: expr) => ($x);
    ($x: expr, $( $y: expr ),+) => {
        std::cmp::max($x, max!($( $y ),+))
    }
}
#[macro_export]
macro_rules! min {
    ($x: expr) => ($x);
    ($x: expr, $( $y: expr ),+) => {
        std::cmp::min($x, min!($( $y ),+))
    }
}
#[derive(Default)]
struct Solver {}
impl Solver {
    #[fastout]
    fn solve(&mut self) {
        // let mut stdin = LineSource::new(BufReader::new(io::stdin()));
        // macro_rules! input(($($tt:tt)*) => (proconio::input!(from &mut stdin, $($tt)*)));
        input! {
            N:i32,
        }

        let black = "#";
        let white = ".";

        let mut blocks = vec![vec![String::new(); N as usize]; N as usize];

        for i in 1..=N {
            let j = N + 1 - i;
            if i <= j {
                let text = if i % 2 == 0 {
                    white.to_string()
                } else {
                    black.to_string()
                };
                for k in i - 1..j {
                    blocks[k as usize][(i - 1) as usize..j as usize].fill(text.clone());
                }
            }
        }
        for i in 0..N as usize {
            println!("{}", blocks[i].join(""));
        }
    }
}

fn main() {
    std::thread::Builder::new()
        .stack_size(128 * 1024 * 1024)
        .spawn(|| Solver::default().solve())
        .unwrap()
        .join()
        .unwrap();
}
